function cosh(num){
	var term1 = Math.pow(Math.E, num);
    var term2 = Math.pow(Math.E, -num);
    return (term1 + term2) / 2;
}

function sinh(num){
	var term1 = Math.pow(Math.E, num);
    var term2 = Math.pow(Math.E, -num);
    return (term1 - term2) / 2;
}

